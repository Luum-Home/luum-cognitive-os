async function countFiles(dir: string, ext: string): Promise<number> {
  try {
    const entries = await readdir(dir);
    return entries.filter((e) => e.endsWith(ext)).length;
  } catch {
    return 0;
  }
}

async function countSkills(dir: string): Promise<number> {
  try {
    const entries = await readdir(dir, { withFileTypes: true });
    return entries.filter((e) => e.isDirectory()).length;
  } catch {
    return 0;
  }
}

async function countDirectories(dir: string): Promise<number> {
  try {
    const entries = await readdir(dir, { withFileTypes: true });
    return entries.filter((e) => e.isDirectory()).length;
  } catch {
    return 0;
  }
}

interface CosConfig {
  projectName: string;
  phase: string;
  version: string;
}

async function readCosConfig(): Promise<CosConfig> {
  try {
    const content = await readFile(
      join(COS_ROOT, "cognitive-os.yaml"),
      "utf-8"
    );
    // Simple YAML extraction without a parser dependency
    const phase = extractYamlValue(content, "phase") || "reconstruction";
    const name = extractYamlValue(content, "name") || "luum-agent-os";
    const version = extractYamlValue(content, "version") || "0.0.0";
    return { projectName: name, phase, version };
  } catch {
    return {
      projectName: "unknown",
      phase: "unknown",
      version: "0.0.0",
    };
  }
}

function extractYamlValue(content: string, key: string): string | null {
  const regex = new RegExp(`^\\s*${key}:\\s*(.+)$`, "m");
  const match = content.match(regex);
  return match ? match[1].trim().replace(/^["']|["']$/g, "") : null;
}

function extractFirstParagraph(markdown: string): string {
  const lines = markdown.split("\n");
  const paragraphLines: string[] = [];
  let started = false;

  for (const line of lines) {
    const trimmed = line.trim();
    // Skip frontmatter and headings
    if (trimmed.startsWith("---") || trimmed.startsWith("#")) {
      if (started) break;
      continue;
    }
    if (trimmed === "") {
      if (started) break;
      continue;
    }
    started = true;
    paragraphLines.push(trimmed);
  }

  return paragraphLines.join(" ").slice(0, 200) || "No description";
}
