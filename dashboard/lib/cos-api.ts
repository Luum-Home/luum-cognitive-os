import { readdir, readFile } from "fs/promises";
import { join, basename } from "path";

/**
 * COS API layer -- Phase 1 reads directly from the filesystem.
 * Phase 2+ will call the COS MCP server via subprocess or HTTP.
 */

const COS_ROOT = process.env.COS_ROOT || join(process.cwd(), "..");

// ---------------------------------------------------------------------------
// Status
// ---------------------------------------------------------------------------

export interface CosStatus {
  projectName: string;
  phase: string;
  cosVersion: string;
  rulesCount: number;
  hooksCount: number;
  skillsCount: number;
  packagesCount: number;
  activeSessions: number;
}

export async function getCosStatus(): Promise<CosStatus> {
  const [rulesCount, hooksCount, skillsCount, packagesCount, config] =
    await Promise.all([
      countFiles(join(COS_ROOT, "rules"), ".md"),
      countFiles(join(COS_ROOT, "hooks"), ".sh"),
      countSkills(join(COS_ROOT, "skills")),
      countDirectories(join(COS_ROOT, "packages")),
      readCosConfig(),
    ]);

  return {
    projectName: config.projectName,
    phase: config.phase,
    cosVersion: config.version,
    rulesCount,
    hooksCount,
    skillsCount,
    packagesCount,
    activeSessions: 0,
  };
}

// ---------------------------------------------------------------------------
// Rules
// ---------------------------------------------------------------------------

export interface CosRule {
  name: string;
  path: string;
}

export async function getRules(): Promise<CosRule[]> {
  const rulesDir = join(COS_ROOT, "rules");
  const rules: CosRule[] = [];

  try {
    const entries = await readdir(rulesDir);
    for (const entry of entries) {
      if (entry.endsWith(".md")) {
        const name = basename(entry, ".md");
        rules.push({ name, path: `rules/${entry}` });
      }
    }
  } catch {
    // Directory may not exist in test environments
  }

  return rules.sort((a, b) => a.name.localeCompare(b.name));
}

// ---------------------------------------------------------------------------
// Skills
// ---------------------------------------------------------------------------

export interface CosSkill {
  name: string;
  description: string;
  trigger?: string;
  autoGenerated: boolean;
}

export async function getSkills(): Promise<CosSkill[]> {
  const skillsDir = join(COS_ROOT, "skills");
  const skills: CosSkill[] = [];

  try {
    const entries = await readdir(skillsDir, { withFileTypes: true });
    for (const entry of entries) {
      if (!entry.isDirectory()) continue;

      const skillPath = join(skillsDir, entry.name, "SKILL.md");
      try {
        const content = await readFile(skillPath, "utf-8");
        const description = extractFirstParagraph(content);
        const trigger = entry.name;
        const autoGenerated = entry.name.startsWith("auto-") ||
          content.includes("auto-generated: true");

        skills.push({
          name: entry.name,
          description,
          trigger,
          autoGenerated,
        });
      } catch {
        // SKILL.md may not exist for all directories
        skills.push({
          name: entry.name,
          description: "No description available",
          autoGenerated: false,
        });
      }
    }
  } catch {
    // Directory may not exist
  }

  return skills.sort((a, b) => a.name.localeCompare(b.name));
}


